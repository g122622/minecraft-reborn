// 每个字符的字节数
static constexpr size_t BYTES_PER_CHAR = 7;
// 字体宽度（位数）
static constexpr size_t FONT_WIDTH_BITS = 5;
// 字体高度
static constexpr size_t FONT_HEIGHT = 7;

Result<void> DefaultAsciiFont::create(Font& font) {
    // 初始化字体图集 (256x256)
    auto result = font.initialize(256);
    if (!result.success()) {
        return result.error();
    }

    // 创建字形提供者
    class DefaultGlyphProvider : public IGlyphProvider {
    public:
        [[nodiscard]] bool getGlyphData(u32 codepoint,
                                        std::vector<u8>& outPixels,
                                        u32& outWidth, u32& outHeight,
                                        f32& outAdvance,
                                        f32& outBearingX,
                                        f32& outBearingY) const override {
            // 只支持ASCII可打印字符 (32-126)
            if (codepoint < 32 || codepoint > 126) {
                return false;
            }

            // 空格特殊处理
            if (codepoint == ' ') {
                outPixels.resize(1, 0); // 1像素透明
                outWidth = 1;
                outHeight = 1;
                outAdvance = 4.0f;
                outBearingX = 0.0f;
                outBearingY = static_cast<f32>(FONT_HEIGHT);
                return true;
            }

            // 获取字符数据
            size_t index = (codepoint - 32) * BYTES_PER_CHAR;
            const u8* charData = &FONT_5x7[index];

            // 找到字符的实际宽度
            u32 actualWidth = 0;
            for (size_t y = 0; y < FONT_HEIGHT; ++y) {
                for (size_t x = 0; x < FONT_WIDTH_BITS; ++x) {
                    if (charData[y] & (1 << (FONT_WIDTH_BITS - 1 - x))) {
                        actualWidth = std::max(actualWidth, static_cast<u32>(x + 1));
                    }
                }
            }
            if (actualWidth == 0) actualWidth = 1;

            // 生成像素数据 (8x8格子，字符在左上角)
            outWidth = actualWidth;
            outHeight = static_cast<u32>(FONT_HEIGHT);
            outPixels.resize(static_cast<size_t>(outWidth) * outHeight, 0);

            for (size_t y = 0; y < FONT_HEIGHT; ++y) {
                for (size_t x = 0; x < actualWidth; ++x) {
                    if (charData[y] & (1 << (FONT_WIDTH_BITS - 1 - x))) {
                        outPixels[y * outWidth + x] = 255; // 白色
                    }
                }
            }

            outAdvance = static_cast<f32>(actualWidth + 1); // +1间距
            outBearingX = 0.0f;
            outBearingY = static_cast<f32>(FONT_HEIGHT);

            return true;
        }

        [[nodiscard]] const std::vector<u32>& getCodepoints() const override {
            static std::vector<u32> codepoints;
            if (codepoints.empty()) {
                for (u32 c = 32; c <= 126; ++c) {
                    codepoints.push_back(c);
                }
            }
            return codepoints;
        }

        [[nodiscard]] u32 getFontHeight() const override {
            return static_cast<u32>(FONT_HEIGHT + 1); // +1基线下方空间
        }

        [[nodiscard]] u32 getAscent() const override {
            return static_cast<u32>(FONT_HEIGHT);
        }
    };

    font.addProvider(std::make_unique<DefaultGlyphProvider>());

    // 预加载所有ASCII字符
    for (u32 c = 32; c <= 126; ++c) {
        font.getGlyph(c);
    }

    return {};
}

const u8* DefaultAsciiFont::getFontData(char c) {
    if (c < 32 || c > 126) {
        return nullptr;
    }
    return &FONT_5x7[(static_cast<size_t>(c) - 32) * BYTES_PER_CHAR];
}

u32 DefaultAsciiFont::generateCharBitmap(char c, std::array<u8, 64>& outPixels) {
    outPixels.fill(0);

    const u8* charData = getFontData(c);
    if (charData == nullptr) {
        return 0;
    }

    u32 actualWidth = 0;
    for (size_t y = 0; y < FONT_HEIGHT; ++y) {
        for (size_t x = 0; x < FONT_WIDTH_BITS; ++x) {
            if (charData[y] & (1 << (FONT_WIDTH_BITS - 1 - x))) {
                outPixels[y * 8 + x] = 255;
                actualWidth = std::max(actualWidth, static_cast<u32>(x + 1));
            }
        }
    }

    return actualWidth > 0 ? actualWidth : 1;
}

